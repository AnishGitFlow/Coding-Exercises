# Demonstration of Basic Arithmetic Operations in Python

# Let's define two numbers for demonstration
x = 10
y = 3

# Perform all the arithmetic operations
addition = x + y
subtraction = x - y
multiplication = x * y
power = x ** y
float_division = x / y
floor_division = x // y
modulus = x % y

# Print all the results
print(f"x = {x}, y = {y}\n")
print(f"Addition (x + y): {addition}")
print(f"Subtraction (x - y): {subtraction}")
print(f"Multiplication (x * y): {multiplication}")
print(f"Power (x ** y): {power}")
print(f"Float Division (x / y): {float_division}")
print(f"Floor Division (x // y): {floor_division}")
print(f"Modulus (x % y): {modulus}")